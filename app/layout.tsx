import type { Metadata } from "next";
import {
  QueryClient,
  dehydrate,
  HydrationBoundary,
} from "@tanstack/react-query";
import SideBar from "@/components/SideBar/SideBar";
import SideBarMobile from "@/components/SideBar/SideBarMobile";
import QueryProvider from "@/query/QueryProvider";
import { categoriesQueries, tagsQueries } from "@/query";
import { AppStateProvider } from "@/context/AppStateContext";
import { SidebarProvider } from "@/context/SidebarContext";
import { QUERY_CLIENT_DEFAULT_OPTIONS } from "@/lib";
import { Header } from "@/ui";
import "@/styles/index.css";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const queryClient = new QueryClient({
    defaultOptions: QUERY_CLIENT_DEFAULT_OPTIONS,
  });

  // 카테고리와 태그 데이터를 미리 패칭하여 초기 상태로 제공
  await Promise.all([
    queryClient.prefetchQuery(categoriesQueries.all()),
    queryClient.prefetchQuery(tagsQueries.all()),
  ]);

  return (
    <html lang="en">
      <body className="antialiased font-normal text-s flex flex-row gap-spacing-10 w-full">
        <AppStateProvider>
          <SidebarProvider defaultOpen={false} defaultDirection="horizontal">
            <QueryProvider>
              <HydrationBoundary state={dehydrate(queryClient)}>
                <div className="flex flex-col w-full">
                  <Header />
                  {children}
                </div>
                {/* Desktop: SideBar만 , Mobile: SideBarMobile만 미디어 쿼리로 내부에서 처리 */}
                <SideBar />
                {/* <SideBarMobile /> */}
              </HydrationBoundary>
            </QueryProvider>
          </SidebarProvider>
        </AppStateProvider>
      </body>
    </html>
  );
}
